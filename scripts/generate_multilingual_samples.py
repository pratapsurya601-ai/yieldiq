"""Phase 0 sample generator for multilingual AI summary review.

Generates AI summaries in en/hi/ta/mr for 5 representative stocks
(TCS, RELIANCE, INFY, HDFCBANK, ITC) and writes them side-by-side to
``docs/multilingual_samples_for_review.md``.

These samples are the artefact a native-speaker reviewer signs off on
BEFORE the MULTILINGUAL_SUMMARIES_ENABLED feature flag is flipped in
Railway. This script does NOT write to ``analysis_cache``,
``model_predictions_history``, or any other prod table — it produces
a markdown document only.

Behaviour:
  * If GROQ_API_KEY is set (in env or .env.local), real summaries are
    generated by hitting Groq.
  * If GROQ_API_KEY is missing, the script writes a clearly-labelled
    MOCK output so the PR can still build, with a TODO instructing the
    user to regenerate before review.

Usage:
  python scripts/generate_multilingual_samples.py
"""
from __future__ import annotations

import os
import sys
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parent.parent
DOCS_PATH = REPO_ROOT / "docs" / "multilingual_samples_for_review.md"

SAMPLE_TICKERS = ["TCS", "RELIANCE", "INFY", "HDFCBANK", "ITC"]
LANGUAGES = ["en", "hi", "ta", "mr"]


def _load_env_local() -> None:
    """Best-effort load of .env.local — no python-dotenv dependency.
    Only sets keys that aren't already in os.environ."""
    env_local = REPO_ROOT / ".env.local"
    if not env_local.exists():
        return
    try:
        for line in env_local.read_text(encoding="utf-8").splitlines():
            line = line.strip()
            if not line or line.startswith("#") or "=" not in line:
                continue
            key, _, val = line.partition("=")
            key = key.strip()
            val = val.strip().strip('"').strip("'")
            if key and key not in os.environ:
                os.environ[key] = val
    except Exception as exc:
        print(f"[warn] could not parse .env.local: {exc}", file=sys.stderr)


